#include <stdio.h>

int main() {
    int i = 1, j = 2, k;  // บรรทัดที่ 1: กำหนดค่าเริ่มต้นให้ตัวแปร i และ j

    k = i + j;            // บรรทัดที่ 2: k = 1 + 2 = 3
    i = i + (k * j);      // บรรทัดที่ 3: i = 1 + (3 * 2) = 7
    j = i / 2;            // บรรทัดที่ 4: j = 7 / 2 = 3 (หารจำนวนเต็ม)
    k = i % 2;            // บรรทัดที่ 5: k = 7 % 2 = 1 (หาเศษ)
    i = (j + k) * 3;      // บรรทัดที่ 6: i = (3 + 1) * 3 = 12

    printf("i = %d, j = %d, k = %d\n", i, j, k);
    return 0;
}