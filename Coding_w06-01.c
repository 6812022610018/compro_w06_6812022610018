#include <stdio.h>
int main() {
    int a = 5, b = 17; // a,b เป็นตัวแปรชนิด int
    float c = 8.5, d = 4.0; // c,d เป็นตัวแปรชนิด float
    
    // บรรทัดที่ 1
    printf("ค่าของ a = %d, b = %d, c = %.2f, d = %.2f\n", a, b, c, d); // แสดงค่าของตัวแปร a, b, c, d

    // บรรทัดที่ 2: ผลของ d + a
    printf("ผลลัพธ์ของ d + a = %.2f\n", d + a); // 4.0 + 5 = 9.0

    // บรรทัดที่ 3: ผลของ a - b
    printf("ผลลัพธ์ของ a - b = %d\n", a - b); // 5 - 17 = -12

    // บรรทัดที่ 4: ผลของ c * d
    printf("ผลลัพธ์ของ c * d = %.1f\n", c * d); // 8.5 * 4.0 = 34.0

    // บรรทัดที่ 5: ผลของ a * c
    printf("ผลลัพธ์ของ a * c = %.1f\n", a * c); // 5 * 8.5 = 42.5

    // บรรทัดที่ 6: ผลของ c / d
    printf("ผลลัพธ์ของ c / d = %.3f\n", c / d); // 8.5 / 4.0 = 2.125

    // บรรทัดที่ 7: ผลของ b / c
    printf("ผลลัพธ์ของ b / c = %.1f\n", b / c); // 17 / 8.5 = 2.0

    // บรรทัดที่ 8: ผลของ a % b
    printf("ผลลัพธ์ของ a %% b = %d\n", a % b); // 5 % 17 = 5

    // บรรทัดที่ 9: ผลของ (int)c % a
    printf("ผลลัพธ์ของ (int)c %% a = %d\n", ((int)c % a)); // 8 % 5 = 3

    // บรรทัดที่ 10: ผลของ (int)c % (int)d
    printf("ผลลัพธ์ของ (int)c %% (int)d = %d\n", ((int)c % (int)d)); // 8 % 4 = 0

    return 0;
}
// สรุป:
// บรรทัดที่ 1 แสดงค่าของตัวแปร a, b, c, d
// บรรทัดที่ 2 แสดงผลลัพธ์ของ d + a = 4.0 + 5 = 9.0
// บรรทัดที่ 3 แสดงผลลัพธ์ของ a - b = 5 - 17 = -12
// บรรทัดที่ 4 แสดงผลลัพธ์ของ c * d = 8.5 * 4.0 = 34.0
// บรรทัดที่ 5 แสดงผลลัพธ์ของ a * c = 5 * 8.5 = 42.5
// บรรทัดที่ 6 แสดงผลลัพธ์ของ c / d = 8.5 / 4.0 = 2.125
// บรรทัดที่ 7 แสดงผลลัพธ์ของ b / c = 17 / 8.5 = 2.0
// บรรทัดที่ 8 แสดงผลลัพธ์ของ a % b = 5 % 17 = 5
// บรรทัดที่ 9 แสดงผลลัพธ์ของ (int)c % a = 8 % 5 = 3
// บรรทัดที่ 10 แสดงผลลัพธ์ของ (int)c % (int)d = 8 % 4 = 0